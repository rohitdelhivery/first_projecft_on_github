package com.delhivery.addition;


public class help {
	
	private int a;
	private int b;
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	
	@Override
	public String toString() {
		return "help [a=" + a + ", b=" + b + "]";
	}
	
	
}
